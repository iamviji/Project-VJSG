    int getData(int x, int y)
    {
        return trackMatrix[x][y];
    }
    
    int getCarCurrentPosH() { return currentCarPosH; }
    int getCarCurrentPosW() { return currentCarPosW; }
    
    public void moveCarLeft()
    {
        trackMatrix[currentCarPosH][currentCarPosW+1] = ROAD;
        trackMatrix[currentCarPosH+1][currentCarPosW+1] = ROAD;
        trackMatrix[currentCarPosH][currentCarPosW-1] = CAR;
        trackMatrix[currentCarPosH+1][currentCarPosW-1] = CAR;
        currentCarPosW -= 1;
    }
    public void moveCarRight()
    {
        trackMatrix[currentCarPosH][currentCarPosW] = ROAD;
        trackMatrix[currentCarPosH+1][currentCarPosW] = ROAD;
        trackMatrix[currentCarPosH][currentCarPosW+2] = CAR;
        trackMatrix[currentCarPosH+1][currentCarPosW+2] = CAR;
        currentCarPosW += 1;
    }
    public void moveCarUp()
    {
        System.out.println("Car going up");
        trackMatrix[currentCarPosH+1][currentCarPosW] = ROAD;
        trackMatrix[currentCarPosH+1][currentCarPosW+1] = ROAD;
        trackMatrix[currentCarPosH-1][currentCarPosW] = CAR;
        trackMatrix[currentCarPosH-1][currentCarPosW+1] = CAR;
        currentCarPosH -= 1;
    }    
    public void moveCarBack()
    {
        trackMatrix[currentCarPosH][currentCarPosW] = ROAD;
        trackMatrix[currentCarPosH][currentCarPosW+1] = ROAD;
        trackMatrix[currentCarPosH+2][currentCarPosW] = CAR;
        trackMatrix[currentCarPosH+2][currentCarPosW+1] = CAR;
        currentCarPosH += 1;
    } 
